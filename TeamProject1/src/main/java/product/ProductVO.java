package product;

public class ProductVO {
    private int product_no;
    private int category;
    private int seller_no;
    private String title;
    private int price;
    private String content;
    private String img;
    private String reg_date;
    private int auction;
    private String sell_location;
    private int product_method;
    private int buyer_no;
    private String end_date;

    public int getProduct_no() {
        return product_no;
    }
    public void setProduct_no(int product_no) {
        this.product_no = product_no;
    }
    public int getCategory() {
        return category;
    }
    public void setCategory(int category) {
        this.category = category;
    }
    public int getSeller_no() {
        return seller_no;
    }
    public void setSeller_no(int seller_no) {
        this.seller_no = seller_no;
    }
    public String getTitle() {
        return title;
    }
    public void setTitle(String title) {
        this.title = title;
    }
    public int getPrice() {
        return price;
    }
    public void setPrice(int price) {
        this.price = price;
    }
    public String getContent() {
        return content;
    }
    public void setContent(String content) {
        this.content = content;
    }
    public String getImg() {
        return img;
    }
    public void setImg(String img) {
        this.img = img;
    }
    public String getReg_date() {
        return reg_date;
    }
    public void setReg_date(String reg_date) {
        this.reg_date = reg_date;
    }
    public int getAuction() {
        return auction;
    }
    public void setAuction(int auction) {
        this.auction = auction;
    }
    public String getSell_location() {
        return sell_location;
    }
    public void setSell_location(String sell_location) {
        this.sell_location = sell_location;
    }
    public int getProduct_method() {
        return product_method;
    }
    public void setProduct_method(int product_method) {
        this.product_method = product_method;
    }
    public int getBuyer_no() {
        return buyer_no;
    }
    public void setBuyer_no(int buyer_no) {
        this.buyer_no = buyer_no;
    }
    public String getEnd_date() {
        return end_date;
    }
    public void setEnd_date(String end_date) {
        this.end_date = end_date;
    }
}
